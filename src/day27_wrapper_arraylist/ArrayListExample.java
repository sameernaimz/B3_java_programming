package day27_wrapper_arraylist;

import java.util.ArrayList;

public class ArrayListExample {
    public static void main(String[] args) {
        ArrayList <Integer> numbers = new ArrayList<>();
        System.out.println(numbers);

        numbers.add(40);
        System.out.println(numbers);

        numbers.add(-40);
        System.out.println(numbers);

        numbers.add(50);

    }
}
