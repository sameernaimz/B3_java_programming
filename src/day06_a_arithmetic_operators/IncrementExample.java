package day06_a_arithmetic_operators;

public class IncrementExample {
    public static void main(String[] args) {

        int x = 0;
        System.out.println(x);

        x = 0;
        System.out.println(x);

        x = x + 1; // took the value of x, which is 0 and then added 1 on it --= > x = 0 + 1;
        System.out.println(x);
    }
}
