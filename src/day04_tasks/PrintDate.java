package day04_tasks;

public class PrintDate {
    public static void main(String[] args) {
        int monthNumber = 4;
        int dayNumber = 1;
        int yearNumber = 2024;
        System.out.println(monthNumber + "/" + dayNumber + "/" + yearNumber);
        System.out.println(monthNumber + "_" + dayNumber + "_" + yearNumber);
        System.out.println(monthNumber + "-" + dayNumber + "-" + yearNumber);
        System.out.println(monthNumber + "|" + dayNumber + "|" + yearNumber);
        System.out.println(monthNumber + "<>" + dayNumber + "<>" + yearNumber);


    }
}
