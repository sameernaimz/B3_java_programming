package day07_relational_operators;

public class OperatorPractice {
    public static void main(String[] args) {
        int b = 10;
        b++; // b b+1
        System.out.println(b);
        b--;
        System.out.println(b);
        System.out.println(b++);
        System.out.println(b--);
        System.out.println(b--);
    }
}
